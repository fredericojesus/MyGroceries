package com.example.mygroceries.database;

public class ShoppingList {
	private long id;
	private long itemId;

	public ShoppingList() {
	};

	public ShoppingList(long itemId) {
		this.itemId = itemId;
	}

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public long getItemId() {
		return itemId;
	}

	public void setItemId(long itemId) {
		this.itemId = itemId;
	}
}
